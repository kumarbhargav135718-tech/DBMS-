-- Q19. Name contains 'RAJ'
SELECT * FROM CUSTOMER WHERE FIRST_NAME LIKE '%RAJ%';
 
-- Q20. CUST_NO in range 1003 to 1005
SELECT * FROM CUSTOMER WHERE CUST_NO BETWEEN 1003 AND 1005;
 
-- Q21. Dates in range 10-JAN-71 to 31-DEC-71
SELECT * FROM CUSTOMER
WHERE B_DATE BETWEEN TO_DATE('10-JAN-71','DD-MON-YY')
                 AND TO_DATE('31-DEC-71','DD-MON-YY');
 
