CREATE TABLE CUSTOMER (
    CUST_NO    NUMBER(4)    CONSTRAINT pk_customer PRIMARY KEY,
    FIRST_NAME VARCHAR2(20) NOT NULL,
    LAST_NAME  VARCHAR2(20),
    ADDRESS    VARCHAR2(20),
    CITY       VARCHAR2(8),
    STATE      VARCHAR2(20),
    PIN        NUMBER(6),
    B_DATE     DATE,
    STATUS     CHAR(1)      CONSTRAINT chk_status CHECK (STATUS IN ('V','I','A'))
);
 
